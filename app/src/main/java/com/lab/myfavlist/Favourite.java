package com.lab.myfavlist;

public class Favourite {

    private String name;
    private String age;

    public Favourite() {
    }

    public Favourite(String name, String age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
